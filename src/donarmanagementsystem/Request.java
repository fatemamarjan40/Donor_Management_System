    private void bloodReqSavebtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bloodReqSavebtnActionPerformed
        try{
             Class.forName("com.microsoft.sqlserver.jdbc.SQLServerDriver");
             Connection connection = DriverManager.getConnection(
                    "jdbc:sqlserver://localhost:1433;databaseName=donorManagementSQLServer;selectMethod=cursor", "sa", "12");
             Statement st = connection.createStatement();
             String query ="insert into REQUEST_BLOOD(Name,Blood_Group,Gender,Contact,Address,District,Donation_Time,Donation_Date)values(?,?,?,?,?,?,?,?)";
             PreparedStatement pst = connection.prepareStatement(query);
            
             
             pst.setString(1, bd_name.getText());
             String bloodGroup= bd_BloodGroup.getSelectedItem().toString();
             pst.setString(2,bloodGroup);
              String BD_Gender = null;
             if(bd_Gmale.isSelected())
             {
                 bd_Gfemale.setSelected(false);
                 BD_Gender="Male";
                 
             }
             if(bd_Gfemale.isSelected())
             {
                   bd_Gmale.setSelected(false);
                   BD_Gender="Female";
                 
             }
             pst.setString(3,BD_Gender);
             pst.setString(4, bd_contact.getText());
             pst.setString(5, bd_address.getText());
             pst.setString(6, bd_district.getText());
             pst.setString(7, bd_time.getText());
             pst.setString(8,((JTextField)bd_date.getDateEditor().getUiComponent()).getText());
             pst.executeUpdate();
             JOptionPane.showMessageDialog(null,"Inserted Sucessfully");
             
          
           
        }
        catch(Exception e){
        JOptionPane.showMessageDialog(null,e);
        }
    }//GEN-LAST:event_bloodReqSavebtnActionPerformed

