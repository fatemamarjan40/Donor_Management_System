    private void savebtn_bloodDonorActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_savebtn_bloodDonorActionPerformed
        // TODO add your handling code here:
     
        try{
             Class.forName("com.microsoft.sqlserver.jdbc.SQLServerDriver");
             Connection connection = DriverManager.getConnection(
                    "jdbc:sqlserver://localhost:1433;databaseName=donorManagementSQLServer;selectMethod=cursor", "sa", "12");
             Statement st = connection.createStatement();
             String query ="insert into BLOOD_DONAR(Name,Blood_Group,Gender,Contact,Address,District,Last_Donation_Date,Age,Qualification)values(?,?,?,?,?,?,?,?,?)";
             PreparedStatement pst = connection.prepareStatement(query);
            
            //date difference 
            SimpleDateFormat obj = new SimpleDateFormat("MM-dd-yyyy");  
            Date date1 = obj.parse(bd_lastdonatedateText.getText());
            Date cur=new Date();
            Date date2 = obj.parse(obj.format(cur));   
            
            // Calucalte time difference in milliseconds   
            double time_difference = date2.getTime() - date1.getTime();  
            // Calucalte time difference in days  
            double days_difference = (time_difference / (1000*60*60*24)) % 365;   
           // System.out.print(days_difference);
            String chck=""; 
            if(days_difference >=90)
            {
                chck="Qualify";
            }
            else
            {
                chck="Not Qualify";
            }
             
             pst.setString(1, name.getText());
             String bloodGroup= bd_BGText.getSelectedItem().toString();
             pst.setString(2,bloodGroup);
              String BD_Gender = null;
             if(bd_Gmale.isSelected())
             {
                 bd_Gfemale.setSelected(false);
                 BD_Gender="Male";
                 
             }
             if(bd_Gfemale.isSelected())
             {
                   bd_Gmale.setSelected(false);
                   BD_Gender="Female";
                 
             }
             pst.setString(3,BD_Gender);
             pst.setString(4, bd_contactNo.getText());
             pst.setString(5, bd_addText.getText());
             pst.setString(6, bd_districtText.getText());
             pst.setString(7, bd_lastdonatedateText.getText());
             pst.setString(8, bd_ageText.getText());
             pst.setString(9, chck);
             pst.executeUpdate();
             JOptionPane.showMessageDialog(null,"Inserted Sucessfully");
             
          
           
        }
        catch(Exception e){
        JOptionPane.showMessageDialog(null,e);
        }
           
    }//GEN-LAST:event_savebtn_bloodDonorActionPerformed

