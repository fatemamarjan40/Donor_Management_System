    private void savebtn_plasmaDonorActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_savebtn_plasmaDonorActionPerformed
        try{
            String gender = null;
             Class.forName("com.microsoft.sqlserver.jdbc.SQLServerDriver");
             Connection connection = DriverManager.getConnection(
                    "jdbc:sqlserver://localhost:1433;databaseName=donorManagementSQLServer;selectMethod=cursor", "sa", "12");
             Statement st = connection.createStatement();
             String query ="insert into QUALIFIED_PLASMA_DONAR(Name,Blood_Group,Gender,Contact,Address,District,Age)values(?,?,?,?,?,?,?)";
             PreparedStatement pst = connection.prepareStatement(query);
            
            
             pst.setString(1, pl_name.getText());
             String bloodGroup= pl_bloodGroup.getSelectedItem().toString();
             pst.setString(2,bloodGroup);
             if(P_Male.isSelected())
             {
                 P_Male.setSelected(false);
                 gender="Male";
                 
             }
             if(P_Female.isSelected())
             {
                   P_Female.setSelected(false);
                   gender="Female";
                 
             }
             pst.setString(3,gender);
             pst.setString(4, pl_contact.getText());
             pst.setString(5, p_address.getText());
             pst.setString(6, pl_district.getText());
             pst.setString(7, pl_age.getText());
             pst.executeUpdate();
             JOptionPane.showMessageDialog(null,"Inserted Sucessfully");
             
          
           
        }
        catch(Exception e){
        JOptionPane.showMessageDialog(null,e);
        }
           
    }//GEN-LAST:event_savebtn_plasmaDonorActionPerformed

